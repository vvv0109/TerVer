#Рост дочерей 175, 167, 154, 174, 178, 148, 160, 167, 169, 170
#Рост матерей  178, 165, 165, 173, 168, 155, 160, 164, 178, 175
#Используя эти данные построить 95% доверительный интервал для разности среднего роста родителей и детей.
import numpy as np
from scipy import stats

daughters = [175, 167, 154, 174, 178, 148, 160, 167, 169, 170]
mothers = [178, 165, 165, 173, 168, 155, 160, 164, 178, 175]

# Рассчитаем разность роста для каждой пары матерь-дочь
height_diffs = np.array(daughters) - np.array(mothers)

# Рассчитаем среднее и стандартное отклонение разности роста
mean = np.mean(height_diffs)
std_dev = np.std(height_diffs, ddof=1) / np.sqrt(len(height_diffs))

# Найдем t-критическое значение для 95% доверительного интервала
t_value = stats.t.ppf(0.975, len(height_diffs) - 1)

# Рассчитаем границы доверительного интервала
lower = mean - t_value * std_dev
upper = mean + t_value * std_dev

print(f"95% доверительный интервал для разности среднего роста: [{lower}, {upper}]")