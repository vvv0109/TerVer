#Измерены значения IQ выборки студентов,
#обучающихся в местных технических вузах:
#131, 125, 115, 122, 131, 115, 107, 99, 125, 111.
#Известно, что в генеральной совокупности IQ распределен нормально.
#Найдите доверительный интервал для математического ожидания с надежностью 0.95.
import numpy as np
from scipy.stats import t

# выборка IQ
sample = np.array([131, 125, 115, 122, 131, 115, 107, 99, 125, 111])

# уровень доверия
confidence = 0.95

# находим среднее значение выборки и ее стандартное отклонение
n = len(sample)
mean = np.mean(sample)
std = np.std(sample, ddof=1)

# находим критическое значение t-статистики
alpha = 1 - confidence
t_stat = t.ppf(1 - alpha / 2, df=n - 1)

# вычисляем пределы доверительного интервала
left = mean - t_stat * std / np.sqrt(n)
right = mean + t_stat * std / np.sqrt(n)

# выводим результат
print(f"95%-й доверительный интервал для истинного значения IQ: ({left:.3f}, {right:.3f})")