#Даны значения величины заработной платы заемщиков банка (zp) и значения их поведенческого кредитного скоринга (ks):
#zp = [35, 45, 190, 200, 40, 70, 54, 150, 120, 110], ks = [401, 574, 874, 919, 459, 739, 653, 902, 746, 832].
#Используя математические операции, посчитать коэффициенты линейной регрессии,
#приняв за X заработную плату (то есть, zp - признак), а за y - значения скорингового балла
#(то есть, ks - целевая переменная). Произвести расчет как с использованием intercept, так и без.
import numpy as np

zp = np.array([35, 45, 190, 200, 40, 70, 54, 150, 120, 110])
ks = np.array([401, 574, 874, 919, 459, 739, 653, 902, 746, 832])

# Коэффициенты регрессии с использованием intercept
b1 = np.cov(zp, ks, ddof=1)[0, 1] / np.var(zp, ddof=1)
b0 = ks.mean() - b1 * zp.mean()
print("Коэффициенты регрессии с intercept: b0 = {:.2f}, b1 = {:.2f}".format(b0, b1))

# Коэффициенты регрессии без intercept
b1 = np.sum((zp - zp.mean()) * (ks - ks.mean())) / np.sum((zp - zp.mean()) ** 2)
b0 = ks.mean() - b1 * zp.mean()
print("Коэффициенты регрессии без intercept: b0 = {:.2f}, b1 = {:.2f}".format(b0, b1))
